<h2>Rice Wine</h2>
<p>

<ul>
<li><p>5 cups short grain rice per gallon
<li><p>1 1/2 cups nuruk per gallon (&alpha; amylase)
<li><p>1 yeast cake per gallon
</ul>

<ol>
<li><p><i>Some people soak the rice 2 hrs or more before beginning.</i>
<li><p><i>Some people cook the rice.  5 cups rice, 4 cups water, cook on med for 15 min, cook on low for 15 min, stir often.</i>
<li><p><i>Some people dry the cooked rice either in a dehydrator (160 &deg;F) or in a sunny, breezy spot for 3 or 4 hrs.  Until the outside is hard but the inside is still moist.</i>
<p>
<li><p>Add the rice (however it is prepared) and bring the volume to 1 gallon with water.
<li><p>Add yeast and nuruk (&alpha; amylase) stir well.
<p><i>Check after several hours, most of the water should be absorbed and the slurry should be a thick paste.</i>
<li><p>Cover and place somewhere warm.
<p>
<li><p>Stir well once a day.  Run to completion (bubbles will stop).  It will get thinner each day.
</ol>

<h3>Chilled for storage, diluted and consumed this way it is 'dong dong ju' - alcohol with things floating in it</h3>
<h3>Strained to remove most solids, diluted and stored this way is 'makgeolli' - rice beer</h3>
<h3>Strained and racked (once a month until happy) to fully clarify is rice wine.</h3>
<h3>Distilled makes soju</h3>

<p>Like most wines, you can add sugar to sweeten it, let it age to dry it and repeatedly rack it to clarify it. 

<h2>Variations</h2>
<ul>
<li><p>Add fruit and veg to your hearts content.
</ul>
