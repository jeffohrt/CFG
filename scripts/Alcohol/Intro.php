<h2>Wine Base</h2>

<ul>
<li><p><i>flavor</i>
<li><p>3 lbs sugar per gallon (unrefined, like corn sugar, is better than table sugar)
<p><i>2 lbs per gallon makes 'lighter' wine.</i>
<p><i>Some people use 2 lbs sugar and 1 lb raisins.</i>
<p><i>Some people use 2 lbs of honey.</i>
<li><p>2 lemons and / or oranges per gallon
<p><i>Some people use 1 1/2 cups of orange juice.</i>
<p><i>Some people use 1 cup of white grape concentrate.</i>
<li><p><i>Some people add 1 oz(ish) cloves and/or 1 oz(ish) raw ginger per gallon for spiced wine.</i>
<li><p>1 yeast cake per gallon
</ul>

<ol>
<li><p>Simmer sugar, lemons and <i>flavor</i> to sterile.
<li><p>Dilute to desired volume, helps to cool mix.
<li><p>Allow mix to cool to room temp(ish) and add yeast.
<li><p>Cover and keep somewhere warm.
<li><p>Run to completion, depends on temp.
<li><p>Taste, adjust sugar (boiled in water to sterilise), and rack for a month.
<p>Repeat each month until happy.
</ol>

<h2>Variations</h2>

<dl>
<dt><h3>Great Grandpa Fralic's Clover Wine</h3>
<dd><p>Great Grandpa Fralic's Clover Wine ... a 4 gallon carboy of clover wine sat behind the wood stove of my Mother's entire childhood.
<dd><p>Her worst day picking clover with her grandfather was the day a hornet (she says bee) flew up her dress.  Her grandfather wouldn't let her strip, and instead just tried to kill it by swatting it through Mother's dress.  This naturally ended in the critter stinging Mother more than a dozen times (hence hornet not bee).

<ul>
<li><p>1 quart of blossoms (ALL green leaves removed) per gallon.
<li><p>The blossoms are simmered (steeped) in water (1:2, 1 quart of blossoms in 2 quarts of water) for at least 1 hr.
<li><p>Great Grandpa strained the tea to remove the petals, some people don't.
</ul>

<dt><h3>Banana Wine</h3>
<dd>
<ul>
<li><p>4 lbs fresh banana's (with skins) per gallon.
<li><p>Some people sterilise peeled and sliced bananas with sugar and lemons.
<li><p>Some people put peeled and sliced bananas in a nylon bag and in the primary fermenter.
</ul>

