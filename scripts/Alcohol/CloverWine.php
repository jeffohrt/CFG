<h2>Clover Wine</h2>
<p>Great Grandpa Fralic's Clover Wine ... a 4 gallon carboy of clover wine sat behind the wood stove of my Mother's entire childhood.

<p>Her worst day picking clover with her grandfather was the day a hornet (she says bee) flew up her dress.  Her grandfather wouldn't let her strip, and instead just tried to kill it by swatting it through Mother's dress.  This naturally ended in the critter stinging Mother more than a dozen times (hence hornet not bee).
<ul>
<li><p>1 quart clover blossoms (no green) per gallon
<li><p>3 lbs corn sugar per gallon
<li><p>2 lemons per gallon
<li><p>1 yeast cake per gallon
</ul>

<ol>
<li><p>The blossoms are simmered (steeped) in water (1:2, 1 quart of blossoms in 2 quarts of water) for at least 1 hr.
<li><p>The 'tea' is strained to remove all the blossoms.
<li><p>Fresh water is added to raise to the final volume.
<li><p>Let water COOL.
<li><p>Stir in sugar, thinly sliced lemons and yeast.
<li><p>Cover and keep somewhere warm.
<li><p>Run to completion, 6-8 wks depending on temp.
<li><p>Taste, adjust sugar, and rack for a month.
<p>Repeat each month until happy.
</ol>

