<h1>Pickles</h1>

<p>Vinegar Pickling - not Salt pickling ...

<p>Preserving with an acid, ph below 4.5 or 4.6, is called pickling.  The most familiar acid is acetic acid - or vinegar - though fermentation can produce lactic acid and accomplish a similar effect.

<p>General, distilled white vinegar, for cooking is 5% acetic acid with a ph of 2.5 - hence many recipes cut it with a bit of water.

<p>Some peeps just use vinegar.

<p>50/50 water and vinegar, pickling spice, up to 50% sugar (1 cup vinegar, 1 cup water, 1 cup sugar).
<p><i>I prefer vastly less sugar - perhaps 1-2 tablespoon per cup of brine</i>
<p>Some peeps use wine or wine vinegar.

<h2>Mustard Pickles</h2>

<p>Whether mustard pickle pickles, mustard beans or mustard zucchini the process is the same :</p>

<dl>
    <dt><p>&rarr; Salting</dt>
    <dd><p>The veg part is salted as a pre-step.  Some use 100% brine, some 50%, some sprinkle the salt on the veg with no brine.  Some is for an hr or two in the fridge and some are for over night in the fridge.</dd>
    
    <dd><p>Salting Overnight</dd>
    
    <dd><p>Here are some common ratios :
    <ul>
        <li><p>12 large cucumbers peeled and sliced, 4 onions chopped, 4 celery stalks chopped, cover with water, add 1/4 cup course salt and brine over night.</p></li>
        <li><p>1/2 the above but same 1/4 cup of course salt and same 12 hr brining</p></li>
        <li><p>10 cups chopped zucchini, 4 cups chopped onions, cover with water, add 5 tbsp (1/3 cup) of pickling salt, brine over night</p></li>
    </ul>
    </dd>
    
    <dd><p>Salting for 1 hr</dd>
    <dd><ul>
        <li><p>12 large cucumbers, sliced, covered in boiling water with 1/2 cup of pickling salt, let stand 1 hr.</p></li>
    </ul></dd>
    
    <dt>&rarr; Drain and Rinse</dt>
    <dt>&rarr; Pack Bottles</dt>
    <dd><p>Remember to leave head space in jars.</p></dd>
    <dt>&rarr; Sauce</dt>
    <dd><p>Remember to leave head space in jars.</p></dd>
    <dd><p>Sauces are a variation on a theme :</p>
    <ul>
        <li><p>4 cups vinegar</p></li>
        <li><p>4 cups white sugar <i>(I like less)</i></p></li>
        <li><p>2 tbsp dry mustard</p></li>
        <li><p>1 tsp mustard seeds</p></li>
        <li><p>1 tsp celery seeds</p></li>
        <li><p>1 tsp tumeric</p></li>
        <li><p>1/4 cup (4 tbsp) to 1/2 cup (8 tbsp) of flour or 1/2 that in cornstarch</p></li>
    </ul>
    <p>Cook / boil / simmer sauce for 15-30 minutes.  Add more or less flour / cornstarch to thicken to desired consistency.</p>
    </dd>
    <dt>&rarr; Top Up and Can</dt>
    <dd><p>Because the acid will kill the botulism and it's spores, no pressure canner is needed.  A simple boiling water bath for an 15 min is sufficient.</p></dd>
</dl>

